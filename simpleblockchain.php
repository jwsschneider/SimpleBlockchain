<?php


use includes\Blockchain;

include_once("./includes/Block.php");
include_once("./includes/Blockchain.php");

sleep(1);

$blockchain = new Blockchain();

$blockchain = $blockchain->load_blockchain("./blockchain.xml");

$new_block = new Block();

$datetime = new DateTime();

$new_block->timestamp = $datetime->format('Y-m-d H:i:s');

$bottom_block = end($blockchain->bc);

if ($bottom_block == null)
    $new_block->last_hash = "N/A";
else
    $new_block->last_hash = $bottom_block->hash;

if ($bottom_block == null)
    $new_block->id = "0";
else {
    $i = intval($bottom_block->id);
    $new_block->id = ($i+1)."";
}

$new_block->hash = $new_block->gen_hash();

$blockchain->add_block($new_block);

$blockchain->save_blockchain("./blockchain.xml");


